miles_to_kilos = int(input('William, how many miles would you like converted to kilometers: '))
kilos = miles_to_kilos*1.6
print(miles_to_kilos,' miles is equal to ', kilos, 'kilometers')

far_to_cels = int(input('What is the temperature: '))
cels = (far_to_cels-32)*5/9
print(far_to_cels,' fahrenheit is equal to ', cels, 'celsius')

gal_to_liters = float(input('How many gallons: '))
liters = gal_to_liters*3.9
print(gal_to_liters,' gallons is equal to ', liters, 'liters')

pounds_to_kilos = float(input('How many pounds: '))
kilograms = pounds_to_kilos*.45
print(pounds_to_kilos,' pounds is equal to ', kilograms, 'kilograms')

inches_to_centi = float(input('How many inches: '))
centi = inches_to_centi*2.54
print(inches_to_centi,' inches is equal to ', centi, 'centimeters')
